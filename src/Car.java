public class Car {
    private String model;
    private String name;
    private int price;

    void setModel(String model){
        this.model=model;
    }
    void setName(String name){
        this.name=name;
    }
      void setPrice(int price){
        this.price=price;
      }
      String getModel(){
        return model;
      }
      String getName(){
        return name;
      }
      int getPrice(){
        return price;
      }
}
