public class School {
    private int number;
    private String teacher;
    private String student;


    void setNumber(int number){
        this.number=number;
    }
    void setTeacher(String teacher){
        this.teacher=teacher;
    }
    void setStudent(String student){
        this.student=student;
    }
    int getNumber(){
        return number;
    }
    String getTeacher(){
        return teacher;
    }
    String getStudent(){
        return student;
    }
}
